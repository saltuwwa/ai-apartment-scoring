-- Запуск: psql -U postgres -d real_estate -f init_furniture_db.sql
-- Или: создайте БД: CREATE DATABASE real_estate; затем выполните этот скрипт.
-- ==============================================================================

DROP TABLE IF EXISTS furniture_catalog;

CREATE TABLE furniture_catalog (
    id SERIAL PRIMARY KEY,
    category VARCHAR(100) NOT NULL,       -- Категория (Диван, Стол, Кровать, Стул и т.д.)
    subcategory VARCHAR(100),             -- Подкатегория (Угловой, Журнальный, Тумба и т.д.)
    brand VARCHAR(100),                   -- Бренд (обобщенный)
    model_name VARCHAR(255) NOT NULL,     -- Название 
    style VARCHAR(100) NOT NULL,          -- Стиль (Scandi, Modern, Classic, Vintage)
    color VARCHAR(50) NOT NULL,           -- Цвет 
    material VARCHAR(100),                -- Материал 
    price_kzt NUMERIC(12, 2) NOT NULL,   -- Стоимость в тенге
    in_stock BOOLEAN DEFAULT TRUE,        -- Наличие
    rating NUMERIC(3, 2) DEFAULT 4.5,     -- Рейтинг
    description TEXT                      -- Описание для сопоставления с фото
);

